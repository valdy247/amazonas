create or replace function public.is_admin(user_uuid uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = user_uuid
      and role = 'admin'
  );
$$;

create policy "users read own profile"
on public.profiles
for select
using (auth.uid() = id or public.is_admin(auth.uid()));

create policy "providers read public reviewer profiles"
on public.profiles
for select
using (
  role in ('reviewer', 'tester')
  and accepted_terms_at is not null
  and coalesce((profile_data ->> 'publicProfile')::boolean, true) = true
);

create policy "reviewers read public provider profiles"
on public.profiles
for select
using (
  role = 'provider'
  and accepted_terms_at is not null
);

create policy "users update own profile"
on public.profiles
for update
using (auth.uid() = id or public.is_admin(auth.uid()));

create policy "users read own membership"
on public.memberships
for select
using (auth.uid() = user_id or public.is_admin(auth.uid()));

create policy "admin upsert memberships"
on public.memberships
for all
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

create policy "users read own kyc"
on public.kyc_checks
for select
using (auth.uid() = user_id or public.is_admin(auth.uid()));

create policy "admin upsert kyc"
on public.kyc_checks
for all
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

create policy "members read active contacts"
on public.provider_contacts
for select
using (is_active = true);

create policy "admin manage contacts"
on public.provider_contacts
for all
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

create policy "admin logs"
on public.admin_audit_logs
for all
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

create policy "reviewers manage own contact history"
on public.reviewer_contact_history
for all
using (auth.uid() = reviewer_id or public.is_admin(auth.uid()))
with check (auth.uid() = reviewer_id or public.is_admin(auth.uid()));

create policy "providers insert contact requests"
on public.reviewer_contact_requests
for insert
with check (auth.uid() = provider_id or public.is_admin(auth.uid()));

create policy "providers read own sent requests"
on public.reviewer_contact_requests
for select
using (auth.uid() = provider_id or auth.uid() = reviewer_id or public.is_admin(auth.uid()));

create policy "providers update own sent requests"
on public.reviewer_contact_requests
for update
using (auth.uid() = provider_id or auth.uid() = reviewer_id or public.is_admin(auth.uid()))
with check (auth.uid() = provider_id or auth.uid() = reviewer_id or public.is_admin(auth.uid()));

create policy "participants read request messages"
on public.request_messages
for select
using (
  exists (
    select 1
    from public.reviewer_contact_requests requests
    where requests.id = request_messages.request_id
      and (requests.provider_id = auth.uid() or requests.reviewer_id = auth.uid() or public.is_admin(auth.uid()))
  )
);

create policy "participants send request messages"
on public.request_messages
for insert
with check (
  auth.uid() = sender_id
  and exists (
    select 1
    from public.reviewer_contact_requests requests
    where requests.id = request_messages.request_id
      and requests.status = 'accepted'
      and (requests.provider_id = auth.uid() or requests.reviewer_id = auth.uid() or public.is_admin(auth.uid()))
  )
);

