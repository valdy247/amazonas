create extension if not exists "pgcrypto";

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text unique,
  first_name text,
  last_name text,
  full_name text,
  phone text,
  profile_data jsonb default '{}'::jsonb,
  role text default 'pending' check (role in ('pending', 'tester', 'reviewer', 'provider', 'admin')),
  accepted_terms_at timestamptz,
  created_at timestamptz default now()
);

alter table public.profiles add column if not exists first_name text;
alter table public.profiles add column if not exists last_name text;
alter table public.profiles add column if not exists phone text;
alter table public.profiles add column if not exists profile_data jsonb default '{}'::jsonb;

create table if not exists public.memberships (
  user_id uuid primary key references public.profiles(id) on delete cascade,
  status text default 'pending_payment' check (status in ('pending_payment', 'paid', 'active', 'suspended')),
  square_customer_id text,
  square_subscription_id text,
  paid_at timestamptz,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.kyc_checks (
  user_id uuid primary key references public.profiles(id) on delete cascade,
  status text default 'pending' check (status in ('pending', 'in_review', 'approved', 'rejected')),
  provider_name text,
  reference_id text,
  reviewed_at timestamptz,
  created_at timestamptz default now()
);

create table if not exists public.provider_contacts (
  id bigint generated by default as identity primary key,
  title text not null,
  network text,
  url text not null,
  contact_methods text,
  notes text,
  is_active boolean default true,
  is_verified boolean default false,
  created_by uuid references public.profiles(id),
  created_at timestamptz default now()
);

alter table public.provider_contacts add column if not exists contact_methods text;
alter table public.provider_contacts add column if not exists is_verified boolean default false;

create table if not exists public.admin_audit_logs (
  id bigint generated by default as identity primary key,
  admin_id uuid references public.profiles(id),
  action text not null,
  target_user_id uuid,
  metadata jsonb,
  created_at timestamptz default now()
);

create table if not exists public.reviewer_contact_history (
  reviewer_id uuid references public.profiles(id) on delete cascade,
  provider_contact_id bigint references public.provider_contacts(id) on delete cascade,
  contacted_at timestamptz default now(),
  primary key (reviewer_id, provider_contact_id)
);

create table if not exists public.reviewer_contact_requests (
  id bigint generated by default as identity primary key,
  provider_id uuid references public.profiles(id) on delete cascade,
  reviewer_id uuid references public.profiles(id) on delete cascade,
  message text,
  request_data jsonb default '{}'::jsonb,
  response_message text,
  status text default 'sent' check (status in ('sent', 'read', 'accepted', 'declined')),
  created_at timestamptz default now(),
  updated_at timestamptz default now(),
  last_activity_at timestamptz default now()
);

alter table public.reviewer_contact_requests add column if not exists request_data jsonb default '{}'::jsonb;
alter table public.reviewer_contact_requests add column if not exists response_message text;
alter table public.reviewer_contact_requests add column if not exists last_activity_at timestamptz default now();
alter table public.reviewer_contact_requests drop constraint if exists reviewer_contact_requests_provider_id_reviewer_id_key;

create table if not exists public.request_messages (
  id bigint generated by default as identity primary key,
  request_id bigint references public.reviewer_contact_requests(id) on delete cascade,
  sender_id uuid references public.profiles(id) on delete cascade,
  body text not null,
  image_url text,
  image_path text,
  created_at timestamptz default now()
);

alter table public.request_messages add column if not exists image_url text;
alter table public.request_messages add column if not exists image_path text;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email, first_name, last_name, full_name, phone)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data ->> 'first_name', ''),
    coalesce(new.raw_user_meta_data ->> 'last_name', ''),
    coalesce(new.raw_user_meta_data ->> 'full_name', ''),
    coalesce(new.raw_user_meta_data ->> 'phone', '')
  );

  insert into public.memberships (user_id)
  values (new.id);

  insert into public.kyc_checks (user_id)
  values (new.id);

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute procedure public.handle_new_user();

